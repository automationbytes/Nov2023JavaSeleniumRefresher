package Day1;

public class ArthimeticOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 6;

        System.out.println(a+b); //10+6
        int c = a+b;
        System.out.println(c);

        System.out.println(a-b);

        System.out.println(a*b);

        System.out.println(a/b);//quotient

        System.out.println(a%b);//remainder
    }
}
