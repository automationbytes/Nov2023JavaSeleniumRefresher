package Day1;

public class ComparsionOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 19;

        System.out.println("Equals "+(a == b)); //equals
        System.out.println(a != b); //not equals

        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a > b);
        System.out.println(a >= b);
    }
}
